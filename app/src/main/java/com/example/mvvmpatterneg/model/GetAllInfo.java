package com.example.mvvmpatterneg.model;

public class GetAllInfo {

    String Brand;
    String ModelName;
    String Segment;
    String SizeSpecification;
    String Type;
    String DesignType;
    String AspectRatio;

    public String getBrand() {
        return Brand;
    }

    public void setBrand(String brand) {
        Brand = brand;
    }

    public String getModelName() {
        return ModelName;
    }

    public void setModelName(String modelName) {
        ModelName = modelName;
    }

    public String getSegment() {
        return Segment;
    }

    public void setSegment(String segment) {
        Segment = segment;
    }

    public String getSizeSpecification() {
        return SizeSpecification;
    }

    public void setSizeSpecification(String sizeSpecification) {
        SizeSpecification = sizeSpecification;
    }

    public String getType() {
        return Type;
    }

    public void setType(String type) {
        Type = type;
    }

    public String getDesignType() {
        return DesignType;
    }

    public void setDesignType(String designType) {
        DesignType = designType;
    }

    public String getAspectRatio() {
        return AspectRatio;
    }

    public void setAspectRatio(String aspectRatio) {
        AspectRatio = aspectRatio;
    }
}
